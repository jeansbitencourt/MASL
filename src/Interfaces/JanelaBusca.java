    private void jButton_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buscarActionPerformed
        object = new Registro();
        if (!"".equals(jTextField_titulo.getText())) {
            object.setNome(jTextField_titulo.getText());
        }
        if (!"".equals(jTextField_generos.getText())) {
            object.setGeneros(jTextField_generos.getText());
        }
        if (!"".equals(jTextField_ano.getText()) && isInteger(jTextField_ano.getText())) {
            object.setAno(Integer.parseInt(jTextField_ano.getText()));
        }
        if (!"".equals(jTextField_epitotal.getText()) && isInteger(jTextField_epitotal.getText())) {
            object.setEpitotal(Integer.parseInt(jTextField_epitotal.getText()));
        }
        if (!"".equals(jTextField_nota.getText()) && isInteger(jTextField_nota.getText())) {
            object.setNota(Integer.parseInt(jTextField_nota.getText()));
        }
        if (!"".equals(jTextField_temporada.getText())) {
            object.setTemporada(jTextField_temporada.getText());
        }
        if (jComboBox_tipo.getSelectedItem() == "Série") {
            object.setTipo("Série");
        } else {
            object.setTipo("Anime");
        }       
        if (jComboBox_drop.getSelectedItem() == "Não") {
            object.setDrop("Não");
        } else {
            object.setDrop("Sim");
        }

        RegistroControl dadosiniciais = new RegistroControl();
        ArrayList<Registro> array = dadosiniciais.search(object);
        montaTabela(array);
    }//GEN-LAST:event_jButton_buscarActionPerformed

