    void montaTabela(ArrayList<Registro> array) {
        DefaultTableModel modelo = (DefaultTableModel) jTable_registros.getModel();
        //if (!array.isEmpty()) {
            int rowCount = modelo.getRowCount();
            //Remove rows one by one from the end of the table
            for (int i = rowCount - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
            for (Registro registro : array) {
                modelo.addRow(new Object[]{registro.getId(), registro.getNome(), registro.getTipo(), registro.getAno(), registro.getTemporada(), registro.getNota(), registro.getGeneros(), registro.getEpitotal(), registro.getEpiatual(), registro.getUltimaModificacao()});
            }
            jTable_registros.getSelectionModel().setSelectionInterval(0, 0);
            jTable_registros.setModel(modelo);
        //}
    }


